package Oops.Inher2;

public class Person {
   
        private String name;
    
        // Constructor
        public Person(String name) {
            this.name = name;
        }
    
        // Getter method for name
        public String getName() {
            return name;
        }
    
        // Setter method for name
        public void setName(String name) {
            this.name = name;
        }
    }
    

